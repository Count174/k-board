function deriveIcon(title) {
  const t = String(title || '').toLowerCase().trim();
  if (!t) return DEFAULT_ICON;
  for (const [emoji, keywords] of RULES) {
    for (const kw of keywords) {
      if (t.includes(kw)) return emoji;
    }
  }
  return DEFAULT_ICON;
}

